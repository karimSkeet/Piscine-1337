int	ft_sqrt(int nb)
{
	int	sq;

	if (nb == 0 || nb == 1)
		return (nb);
	sq = 2;
	while (sq <= 46340)
	{
		if (sq * sq == nb)
			return (sq);
		sq++;
	}
	return (0);
}

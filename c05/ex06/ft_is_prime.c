int	ft_is_prime(int nb)
{
	int	d;
	int	i;

	d = 0;
	i = 2;
	if (nb < 2)
		return (0);
	else if (nb == 2)
		return (1);
	else if (nb > 2)
	{
		while (i > 1 && i < nb)
		{
			if (nb % i == 0)
				d++;
			i++;
		}
	}
	if (d == 0)
		return (1);
	else
		return (0);
}

void	ft_rev_int_tab(int *tab, int size)
{
	int		i;
	int		c;
	int		mid;

	i = 0;
	mid = size / 2;
	while (i < mid)
	{
		c = tab[i];
		tab[i] = tab[size - 1 - i];
		tab[size - 1 - i] = c;
		i++;
	}
}
